function pushbutton1_Callback(hObject, eventdata, handles)

usu=get(handles.edit1,'string');
usuario='lady';
usuario1='cristian';
con=get(handles.edit2,'string');
contra='1234';
contra1='2345';
u1=strcmp(usu,usuario1);
u=strcmp(usu,usuario);
c=strcmp(con,contra);
c1=strcmp(con,contra1);
if (u==1 && c==1)
    msgbox('bienvenido');
    set(handles.edit1,'string','');
    set(handles.edit2,'string','');
    set(handles.uipanel1, 'Visible','off');
    set(handles.uipanel2, 'Visible','on');
else
    if (u1==1 && c1==1)
    msgbox('bienvenido');
    set(handles.edit1,'string','');
    set(handles.edit2,'string','');
    set(handles.uipanel1, 'Visible','off');
    set(handles.uipanel2, 'Visible','on');
    else 
     msgbox('usuario o contraseña incorrecta');
    end
end 

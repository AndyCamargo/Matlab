    return
end 
formato={'*.jpg','JPGE(*.jpg)';'*.tif','TIFF(*.tif)'};
[nomb,ruta]=uiputfile(formato,'Guardar imagen');
if nomb==0 
    return 
end
fname=fullfile(ruta,nomb);
imwrite(save,fname);
datos={nom,ape,ced,dir,tel,fname};
insert(conn,'registro',columnas,datos);
close(conn);
